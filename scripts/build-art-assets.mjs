import { mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { basename, dirname, extname, resolve } from "node:path";

const projectRoot = resolve(new URL("..", import.meta.url).pathname);
const imageDir = resolve(projectRoot, "public/art/refusalrail");
const outPath = resolve(projectRoot, "src/generated/art-assets.ts");

const files = readdirSync(imageDir)
  .filter((file) => /\.(png|jpe?g|webp)$/i.test(file))
  .sort();

const entries = files.map((file) => {
  const ext = extname(file).toLowerCase();
  const mimeType = ext === ".jpg" || ext === ".jpeg" ? "image/jpeg" : ext === ".webp" ? "image/webp" : "image/png";
  const key = `/art/refusalrail/${basename(file)}`;
  const base64 = readFileSync(resolve(imageDir, file)).toString("base64");
  return [key, { mimeType, base64 }];
});

const output = `// Generated by scripts/build-art-assets.mjs. Do not edit by hand.
export const artAssets: Record<string, { mimeType: string; base64: string }> = ${JSON.stringify(
  Object.fromEntries(entries),
  null,
  2
)};
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, output);
console.log(`wrote ${outPath} with ${entries.length} art assets`);
